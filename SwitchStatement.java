//A simple snippet using the 'switch' statement
package SwitchStatementEx1;


public class SwitchStatement {
    public static void main(String [] args){
   
        int switchNum = 50;
        
        switch(switchNum){
            
            case 10:System.out.println("This is 50");
            break;
            
            case 50:System.out.println("This is really the integer 50");
            break;
            
            default: System.out.println("Sorry for the runaround...not");
        
        
    }
    
}
}
